function updateLanguageToggle() {
    const toggle = document.getElementById('lang-toggle');
    if (toggle) {
        toggle.classList.toggle('active', currentLanguage === 'ar');
        const enBtn = toggle.querySelector('[data-lang="en"]');
        const arBtn = toggle.querySelector('[data-lang="ar"]');
        if (enBtn && arBtn) {
            enBtn.classList.toggle('active', currentLanguage === 'en');
            arBtn.classList.toggle('active', currentLanguage === 'ar');
        }
    }
}

